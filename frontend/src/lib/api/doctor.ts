function mapAppointment(a: any): AppointmentWithPatient {
  return {
    id: a.id,
    doctorId: a.doctorId ?? a.doctor_id,
    appointmentDate: a.appointmentDate ?? a.appointment_date,
    appointmentTime: a.appointmentTime ?? a.appointment_time,
    status: a.status,
    flowStatus: deriveFlowStatus(a),
    reason: a.reason,
    durationMinutes: a.durationMinutes ?? 30,
    isUrgent: a.isUrgent ?? false,
    patient: a.patient
      ? {
          id: a.patient.id,
          nombre: a.patient.nombre,
          historialNumero: a.patient.historialNumero,
          edad: a.patient.edad,
          condicionesCronicas: a.patient.condicionesCronicas ?? [],
          tieneAlertas: (a.patient.alergias?.length ?? 0) > 0,
        }
      : undefined,
  };
}

function deriveFlowStatus(a: any): FlowStatus {
  if (a.status === 'COMPLETADA') return 'completed';
  if (a.status === 'EN_CONSULTA') return 'in_progress';
  if (a.status === 'EN_ESPERA' && a.vitalSignsRegistered) return 'ready';
  return 'waiting_vitals';
}

function buildAlerts(patient: any, vitals: any) {
  const alerts: { tipo: 'danger' | 'warning' | 'info'; mensaje: string }[] = [];
  if (patient?.alergias?.length) {
    alerts.push({ tipo: 'danger', mensaje: `Alergia: ${patient.alergias.join(', ')}` });
  }
  if (vitals?.presionArterial) {
    const sys = parseInt(vitals.presionArterial.split('/')[0]);
    if (sys >= 140) alerts.push({ tipo: 'warning', mensaje: 'PA elevada en este control' });
  }
  return alerts;
}

function parseActiveMedications(records: any[]) {
  if (!records?.length) return [];
  const latest = records[0];
  if (!latest?.medications) return [];
  return latest.medications
    .split(';')
    .map((m: string) => m.trim())
    .filter(Boolean)
    .map((m: string) => ({ nombre: m, dosis: '', frecuencia: '' }));
}

